class Command(BaseCommand):
    help = '导入 LeetCode Hot 100 题目'

    def handle(self, *args, **options):
        created_count = 0
        for number, title, difficulty, category, time_std in HOT_100:
            _, created = Problem.objects.get_or_create(
                number=number,
                defaults={
                    'title': title,
                    'difficulty': difficulty,
                    'category': category,
                    'time_standard': time_std,
                    'leetcode_url': f'https://leetcode.cn/problems/',
                }
            )
            if created:
                created_count += 1

        self.stdout.write(self.style.SUCCESS(
            f'导入完成：新增 {created_count} 题，共 {len(HOT_100)} 题'
        ))
